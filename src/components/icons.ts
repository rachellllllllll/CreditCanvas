/** Build once: emoji â†’ keywords */
export const ICON_SEARCH_MAP: Record<string, string[]> =
  ICON_CATEGORIES.reduce<Record<string, string[]>>((m, cat) => {
    cat.icons.forEach(i => {
      m[i.emoji] = [...(m[i.emoji] || []), ...i.keywords, cat.label];
    });
    return m;
  }, {});

/** Search icons by Hebrew query â€“ returns matching emojis */
export function searchIcons(query: string): string[] {
  if (!query) return ICONS;
  const q = query.toLowerCase();
  return Object.entries(ICON_SEARCH_MAP)
    .filter(([, kw]) => kw.some(k => k.includes(q)))
    .map(([emoji]) => emoji);
}
